    private void tbVentasPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_tbVentasPropertyChange

        if (tbVentas.getRowCount() != 0) {
            int nCantidad = 0;
            int nPrecio = 0;
          

            for (int i = 0; i < tbVentas.getRowCount(); i++) {

                if (tbVentas.getSelectedRow() == i) {
                    nCantidad = (int) tbVentas.getValueAt(i, 2);
                    nPrecio = (int) tbVentas.getValueAt(i, 3);

                }
            }
        }


    }//GEN-LAST:event_tbVentasPropertyChange


