    private void inputDineroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputDineroActionPerformed
//AQUI VA EL CODIGO DEL LABEL VUELTO

        metodosProductos metP = new metodosProductos();
        int totalBoleta = metP.totalVenta(inputDinero, tbVentas);
        int dinero = Integer.parseInt(inputDinero.getText());
        int vuelto = metP.Vuelto(totalBoleta, dinero);
        if (dinero >= totalBoleta) {
            lblVuelto.setText(String.valueOf(vuelto));
        } else {
            JOptionPane.showMessageDialog(rootPane, "EL DINERO INGRESADO NO PUEDE SER INFERIOR A LA COMPRA");
        }


    }//GEN-LAST:event_inputDineroActionPerformed

